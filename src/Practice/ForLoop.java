package Practice;

public class ForLoop {
	
	public static void main(String[] args) {
		
		//Pring 10 to 1
		//10 ----1
		for(int k=10;k>=1;k--) {
			System.out.println("The value of k" +k);
		}
		// Print 10 to -10
		for (int j=10;j>=-10;j--){
			System.out.println("The value of j" +j);
		}
			
		for (int n=1;n<=20;n=n+2) {
		
			System.out.println("The value of n " +n);
		}	
	
	
	}
	
	

}
